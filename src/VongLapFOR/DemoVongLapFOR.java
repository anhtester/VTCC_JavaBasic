package VongLapFOR;

public class DemoVongLapFOR {

    public static void main(String[] args) {

        //Khai báo vòng lặp FOR đơn giản
        for (int i = 1; i <= 10; i += 2) {
            //Code cái gì đó mà bạn muốn nó lặp lại
            System.out.println(i);
            System.out.println("Java");
        }

    }

}
